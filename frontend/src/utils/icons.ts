/**
 * 渲染图标组件
 * @param name 图标名称
 * @returns 渲染函数
 */
export function renderIcon(name: string) {
  return () => {
    if (iconSet[name]) {
      return h('div', {
        class: 'sidebar-icon',
        innerHTML: iconSet[name]
      });
    }
    return h('div', { class: 'sidebar-icon' });
  };
}

/**
 * 根据数据类型自动选择合适的图标
 * @param type 数据类型
 * @returns 图标名称
 */
export function getIconByType(type: string): string {
  switch (type.toLowerCase()) {
    case 'student':
      return 'PersonOutline';
    case 'class':
      return 'PeopleOutline';
    case 'record':
      return 'DocumentTextOutline';
    case 'item':
      return 'ListOutline';
    case 'statistics':
      return 'BarChartOutline';
    case 'notification':
      return 'NotificationsOutline';
    case 'setting':
      return 'SettingsOutline';
    case 'success':
      return 'CheckmarkCircleOutline';
    case 'warning':
      return 'AlertCircleOutline';
    case 'error':
      return 'CloseCircleOutline';
    default:
      return 'InformationCircleOutline';
  }
} 